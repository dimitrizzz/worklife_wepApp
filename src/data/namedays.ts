export function getTodayNamedays(): string[] {
    const now = new Date();
    const mm = String(now.getMonth() + 1).padStart(2, "0");
    const dd = String(now.getDate()).padStart(2, "0");
    return namedays[`${mm}-${dd}`] ?? [];
}


export function getNamedaysByDate(month: number, day: number): string[] {
    const mm = String(month).padStart(2, "0");
    const dd = String(day).padStart(2, "0");
    return namedays[`${mm}-${dd}`] ?? [];
}


export function findNameday(name: string): string[] {
    const results: string[] = [];
    for (const [date, names] of Object.entries(namedays)) {
        if (names.some((n) => n.toLowerCase() === name.toLowerCase())) {
            results.push(date);
        }
    }
    return results;
}


export function getAllNamedays(): Record<string, string[]> {
    return { ...namedays };
}
